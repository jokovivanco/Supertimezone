const DateConverter = {
  monthToString(dateIndex) {
    const months = [
      'January',
      'February',
      'March',
      'April',
      'May',
      'June',
      'July',
      'August',
      'September',
      'October',
      'November',
      'December',
    ];
    return months[dateIndex];
  },
};

export default DateConverter;
